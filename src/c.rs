pub(crate) const SECOND: i64 = 1;
pub(crate) const MINUTE: i64 = 60 * SECOND;
pub(crate) const HOUR: i64 = 60 * MINUTE;
pub(crate) const DAY: i64 = 24 * HOUR;

pub(crate) const SECOND_F64: f64 = SECOND as f64;
pub(crate) const MINUTE_F64: f64 = MINUTE as f64;
pub(crate) const HOUR_F64: f64 = HOUR as f64;
pub(crate) const DAY_F64: f64 = DAY as f64;
